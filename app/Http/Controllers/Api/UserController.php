            $user['gifts'] = Gift::where('userId', $user->id)->with('gifts')->get();  
            $user['gifts']->makeHidden(['created_at','updated_at','userId','giftId']);
            
        }
        $users->makeHidden(['created_at','updated_at','userId','giftId']);
        return response()->json([
            'users' => $users
